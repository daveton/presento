# 结构说明（用于Prompt理解）
STRUCTURE_GUIDE = {
    "封面": "标题页，突出核心主题",
    "问题": "为什么要做这件事？核心痛点是什么？",
    "背景": "当前情况和面临的挑战",
    "目标": "要达到什么结果？",
    "方法": "怎么做的？核心思路",
    "做法": "具体执行方式",
    "原因1": "第一个关键原因",
    "原因2": "第二个关键原因",
    "能力1": "核心能力一：是什么？怎么做到？",
    "能力2": "核心能力二：是什么？怎么做到？",
    "能力3": "核心能力三：是什么？怎么做到？",
    "能力4": "核心能力四：是什么？怎么做到？",
    "方法1": "第一个关键方法",
    "方法2": "第二个关键方法",
    "方法3": "第三个关键方法",
    "方案": "解决方案是什么？",
    "市场": "市场机会和规模",
    "优势": "核心优势和壁垒",
    "结果": "最终成果和数据",
    "关键成果": "最重要的几个成果",
    "结论": "核心结论",
    "总结": "要点回顾和行动建议",
    "经验总结": "学到了什么？有什么建议？"
}


def get_structure(intent: str) -> list:
    """
    根据意图获取对应的PPT结构
    """
    return STRUCTURES.get(intent, STRUCTURES["explain"])


def get_structure_description(intent: str) -> str:
    """
    获取结构的详细描述（用于Prompt）
    包含每个节点的说明
    """
    structure = get_structure(intent)

    lines = []
    for node in structure:
        desc = STRUCTURE_GUIDE.get(node, node)
        lines.append(f"- {node}: {desc}")

    return "\n".join(lines)
